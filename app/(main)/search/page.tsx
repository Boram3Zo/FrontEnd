"use client";

import { Header } from "@/app/_components/layout/Header";
import { BottomNavigation } from "@/app/_components/layout/BottomNavigation";
import { Card } from "@/app/_components/ui/Card";
import { Input } from "@/app/_components/ui/Input";
import { Button } from "@/app/_components/ui/Button";
import { Search, MapPin, Clock, Heart, X } from "lucide-react";
import { useState, useEffect, useCallback } from "react";
import { useSearchParams } from "next/navigation";
import Link from "next/link";
import { SafeImage } from "@/app/_components/ui/SafeImage";

interface MapDTO {
  [key: string]: unknown;
}

interface PhotoDTO {
  url?: string;
  [key: string]: unknown;
}

interface PostReadDTO {
  postId: number;
  memberId: number;
  title: string;
  region: string;
  duration: string;
  distance: number;
  content: string;
  theme: string;
  likeCount: number;
  hashtagList: string[];
  map: MapDTO;
  photoList: PhotoDTO[];
}

interface SearchResponse {
  success: boolean;
  data: {
    boardPage: {
      content: PostReadDTO[];
      totalElements: number;
      totalPages: number;
      size: number;
      number: number;
    };
    searchWord: string;
    searchType: string;
  };
}

export default function SearchPage() {
	const searchParams = useSearchParams();
	const [searchQuery, setSearchQuery] = useState("");
	const [searchResults, setSearchResults] = useState<PostReadDTO[]>([]);
	const [isSearching, setIsSearching] = useState(false);
	const [hasSearched, setHasSearched] = useState(false);
	const [recentSearches, setRecentSearches] = useState<string[]>([]);
	const [searchKeyword, setSearchKeyword] = useState("");

	// ÏÑ∏ÏÖò Ïä§ÌÜ†Î¶¨ÏßÄÏóêÏÑú ÏµúÍ∑º Í≤ÄÏÉâÏñ¥ Î∂àÎü¨Ïò§Í∏∞
	useEffect(() => {
		const stored = sessionStorage.getItem("recentSearches");
		if (stored) {
			setRecentSearches(JSON.parse(stored));
		}
	}, []);  // ÏµúÍ∑º Í≤ÄÏÉâÏñ¥Î•º ÏÑ∏ÏÖò Ïä§ÌÜ†Î¶¨ÏßÄÏóê Ï†ÄÏû•
  const saveRecentSearch = useCallback((query: string) => {
    if (!query.trim()) return;

    const trimmedQuery = query.trim();
    setRecentSearches(prevSearches => {
      const updated = [
        trimmedQuery,
        ...prevSearches.filter((item) => item !== trimmedQuery),
      ].slice(0, 5);
      sessionStorage.setItem("recentSearches", JSON.stringify(updated));
      return updated;
    });
  }, []);

  // ÏµúÍ∑º Í≤ÄÏÉâÏñ¥ ÏÇ≠Ï†ú
  const removeRecentSearch = useCallback((query: string) => {
    setRecentSearches(prevSearches => {
      const updated = prevSearches.filter((item) => item !== query);
      sessionStorage.setItem("recentSearches", JSON.stringify(updated));
      return updated;
    });
  }, []);

  // Í≤ÄÏÉâ API Ìò∏Ï∂ú
  const performSearch = useCallback(async (query: string) => {
    if (!query.trim()) return;

    setIsSearching(true);
    setSearchKeyword(query.trim());

    try {
      const response = await fetch(
        `${
          process.env.NEXT_PUBLIC_API_BASE_URL
        }/post/search?page=0&title=${encodeURIComponent(query.trim())}`,
        {
          method: "GET",
          credentials: "include",
          headers: {
            "Content-Type": "application/json",
          },
        }
      );

      if (!response.ok) {
        throw new Error(`Í≤ÄÏÉâ API Ìò∏Ï∂ú Ïã§Ìå®: ${response.status}`);
      }

      const result: SearchResponse = await response.json();

      if (result.success) {
        setSearchResults(result.data.boardPage.content);
        saveRecentSearch(query.trim());
        setHasSearched(true);
      } else {
        console.error("Í≤ÄÏÉâ Ïã§Ìå®:", result);
        setSearchResults([]);
        setHasSearched(true);
      }
    } catch (error) {
      console.error("Í≤ÄÏÉâ Ï§ë Ïò§Î•ò Î∞úÏÉù:", error);
      setSearchResults([]);
      setHasSearched(true);
    } finally {
      setIsSearching(false);
    }
  }, [saveRecentSearch]);

  // URL ÏøºÎ¶¨ ÌååÎùºÎØ∏ÌÑ∞ÏóêÏÑú Í≤ÄÏÉâÏñ¥ Í∞ÄÏ†∏Ïò§Í∏∞ Î∞è ÏûêÎèô Í≤ÄÏÉâ
  useEffect(() => {
    const queryFromUrl = searchParams.get("q");
    if (queryFromUrl && queryFromUrl !== searchKeyword) {
      setSearchQuery(queryFromUrl);
      performSearch(queryFromUrl);
    }
  }, [searchParams, performSearch, searchKeyword]);

  // ÏóîÌÑ∞ ÌÇ§ Í≤ÄÏÉâ
  const handleKeyPress = (e: React.KeyboardEvent) => {
    if (e.key === "Enter") {
      performSearch(searchQuery);
    }
  };

  // Í≤ÄÏÉâ Î≤ÑÌäº ÌÅ¥Î¶≠
  const handleSearchClick = () => {
    performSearch(searchQuery);
  };

  // ÏµúÍ∑º Í≤ÄÏÉâÏñ¥ ÌÅ¥Î¶≠
  const handleRecentSearchClick = (query: string) => {
    setSearchQuery(query);
    performSearch(query);
  };

  // ÏãúÍ∞Ñ Ìè¨Îß∑ÌåÖ Ìï®Ïàò
  const formatDuration = (duration: string) => {
    if (!duration) return "Ï†ïÎ≥¥ ÏóÜÏùå";
    const parts = duration.split(":");
    const hours = parseInt(parts[0]);
    const minutes = parseInt(parts[1]);

    if (hours > 0) {
      return `${hours}ÏãúÍ∞Ñ ${minutes}Î∂Ñ`;
    }
    return `${minutes}Î∂Ñ`;
  };

  // Í±∞Î¶¨ Ìè¨Îß∑ÌåÖ Ìï®Ïàò
  const formatDistance = (distance: number) => {
    if (!distance) return "Ï†ïÎ≥¥ ÏóÜÏùå";
    if (distance >= 1) {
      return `${distance.toFixed(1)}km`;
    }
    return `${(distance * 1000).toFixed(0)}m`;
  };

  return (
    <div className="min-h-screen bg-gradient-to-b from-blue-50 to-indigo-50">
      <Header />

      <main className="pb-20 px-4">
        {/* Search Header */}
        <div className="py-6">
          <h1 className="text-2xl font-bold text-gray-800 mb-4 text-center">
            ÏÇ∞Ï±Ö ÏΩîÏä§ Í≤ÄÏÉâ üîç
          </h1>

          <div className="relative flex gap-2">
            <div className="relative flex-1">
              <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-5 w-5 text-gray-400" />
              <Input
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                onKeyPress={handleKeyPress}
                placeholder="Í≤ÄÏÉâÌï† Ï†úÎ™©ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî"
                className="pl-10 py-3 text-base rounded-xl border-2 border-gray-200 focus:border-orange-400"
                disabled={isSearching}
              />
            </div>
            <Button
              onClick={handleSearchClick}
              disabled={isSearching || !searchQuery.trim()}
              className="px-6 py-3 bg-orange-500 hover:bg-orange-600 text-white rounded-xl"
            >
              {isSearching ? "Í≤ÄÏÉâÏ§ë..." : "Í≤ÄÏÉâ"}
            </Button>
          </div>
        </div>

        {/* Í≤ÄÏÉâ Í≤∞Í≥º */}
        {hasSearched && (
          <div className="mb-6">
            <h2 className="text-lg font-semibold text-gray-800 mb-4">
              &ldquo;{searchKeyword}&rdquo;Î°ú Í≤ÄÏÉâÎêú Í≤∞Í≥º (
              {searchResults.length}Í∞ú)
            </h2>

            {searchResults.length > 0 ? (
              <div className="space-y-3">
                {searchResults.map((post) => (
                  <Link key={post.postId} href={`/course/${post.postId}`}>
                    <Card className="p-4 bg-white hover:shadow-lg transition-all duration-300 cursor-pointer">
                      <div className="flex items-center gap-3">
                        <div className="w-16 h-16 bg-gradient-to-br from-green-200 to-green-400 rounded-lg overflow-hidden">
                          <SafeImage
                            src={
                              post.photoList?.[0]?.url ||
                              "/placeholder-course.png"
                            }
                            alt={post.title}
                            width={64}
                            height={64}
                            className="w-full h-full object-cover"
                          />
                        </div>
                        <div className="flex-1">
                          <h3 className="text-sm font-semibold text-gray-800 mb-1 text-left">
                            {post.title}
                          </h3>
                          <div className="flex items-center gap-3 text-xs text-gray-600 mb-1">
                            <div className="flex items-center gap-1">
                              <MapPin className="h-3 w-3" />
                              <span>{post.region}</span>
                            </div>
                            <div className="flex items-center gap-1">
                              <Clock className="h-3 w-3" />
                              <span>{formatDuration(post.duration)}</span>
                            </div>
                          </div>
                          <div className="flex items-center justify-between">
                            <div className="flex items-center gap-1">
                              <span className="text-blue-500">üìè</span>
                              <span className="text-xs font-medium text-gray-800">
                                {formatDistance(post.distance)}
                              </span>
                            </div>
                            <div className="flex items-center gap-1">
                              <Heart className="h-3 w-3 text-gray-400" />
                              <span className="text-xs text-gray-500">
                                {post.likeCount}
                              </span>
                            </div>
                          </div>
                          {post.hashtagList && post.hashtagList.length > 0 && (
                            <div className="flex flex-wrap gap-1 mt-2">
                              {post.hashtagList
                                .slice(0, 3)
                                .map((tag, index) => (
                                  <span
                                    key={index}
                                    className="text-xs bg-orange-100 text-orange-600 px-2 py-1 rounded-full"
                                  >
                                    #{tag}
                                  </span>
                                ))}
                            </div>
                          )}
                        </div>
                      </div>
                    </Card>
                  </Link>
                ))}
              </div>
            ) : (
              <Card className="p-8 text-center bg-gray-50">
                <div className="text-4xl mb-4">üîç</div>
                <h3 className="text-lg font-medium text-gray-600 mb-2">
                  Í≤ÄÏÉâ Í≤∞Í≥ºÍ∞Ä ÏóÜÏäµÎãàÎã§
                </h3>
                <p className="text-sm text-gray-500">
                  Îã§Î•∏ ÌÇ§ÏõåÎìúÎ°ú Í≤ÄÏÉâÌï¥Î≥¥ÏÑ∏Ïöî
                </p>
              </Card>
            )}
          </div>
        )}

        {/* Recent Searches - Í≤ÄÏÉâÌïòÏßÄ ÏïäÏïòÏùÑ ÎïåÎßå ÌëúÏãú */}
        {!hasSearched && recentSearches.length > 0 && (
          <div className="mb-6">
            <h2 className="text-lg font-semibold text-gray-800 mb-3">
              ÏµúÍ∑º Í≤ÄÏÉâ
            </h2>
            <div className="space-y-2">
              {recentSearches.map((search, index) => (
                <Card
                  key={index}
                  className="p-3 bg-white hover:bg-gray-50 cursor-pointer"
                >
                  <div className="flex items-center justify-between">
                    <span
                      className="text-gray-700 flex-1"
                      onClick={() => handleRecentSearchClick(search)}
                    >
                      {search}
                    </span>
                    <button
                      onClick={() => removeRecentSearch(search)}
                      className="text-gray-400 hover:text-gray-600 p-1"
                    >
                      <X className="h-4 w-4" />
                    </button>
                  </div>
                </Card>
              ))}
            </div>
          </div>
        )}

        {/* Í≤ÄÏÉâ ÏïàÎÇ¥ Î©îÏãúÏßÄ - Í≤ÄÏÉâÌïòÏßÄ ÏïäÏïòÍ≥† ÏµúÍ∑º Í≤ÄÏÉâÏñ¥Í∞Ä ÏóÜÏùÑ Îïå */}
        {!hasSearched && recentSearches.length === 0 && (
          <Card className="p-8 text-center bg-gray-50">
            <div className="text-4xl mb-4">üîç</div>
            <h3 className="text-lg font-medium text-gray-600 mb-2">
              Í≤ÄÏÉâÏñ¥Î•º ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî
            </h3>
            <p className="text-sm text-gray-500">
              ÏõêÌïòÎäî ÏÇ∞Ï±Ö ÏΩîÏä§Î•º Ï∞æÏïÑÎ≥¥ÏÑ∏Ïöî
            </p>
          </Card>
        )}
      </main>

      <BottomNavigation />
    </div>
  );
}
